    public PluginConfigurationDialog(java.awt.Frame parent, IMarauroaApplication app) {
        super(parent, true);
        this.app = app;
        initComponents();
        //Clean up the table
        for (int i = 0; i < pluginTable.getModel().getRowCount(); i++) {
            ((DefaultTableModel) pluginTable.getModel()).removeRow(0);
        }
        //Repopulate
        Application application = DataBaseManager.findApplication(app).get(0);
        for (MarauroaServerExtension extension
                : Lookup.getDefault().lookupAll(MarauroaServerExtension.class)) {
            boolean found = false;
            for (ServerPlugin plugin : application.getServerPluginList()) {
                if (plugin.getName().equals(extension.getName())) {
                    found = true;
                    break;
                }
            }
            ((DefaultTableModel) pluginTable.getModel())
                    .addRow(new Object[]{found, extension.getName()});
        }
    }

